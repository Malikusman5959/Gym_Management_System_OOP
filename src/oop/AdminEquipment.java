    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        
        String s = "Following errors were found : ";
        
      if (HelperMethods.isAlpha(jTextField1.getText()) == false)
      {
          s += "\n - Invalid Name."; 
      }
      if (HelperMethods.isNumber(jTextField2.getText()) == false )
      {
          s += "\n - Invalid Price";
      }
     
      
      if (s.length() > 35)
      {
          //display error message
          JOptionPane.showMessageDialog(rootPane, s);
      }
      
      else
      {
          if (Integer.parseInt(jTextField2.getText()) * (jComboBox4.getSelectedIndex()+1) > gym.locker.getAmount())
          {
              JOptionPane.showMessageDialog(rootPane, "You donot have enough Cash in locker to complete this Purchase!");
          }
          else
          {
            if (jComboBox7.getSelectedItem().toString().equalsIgnoreCase("Machine"))
            {
               gym.listOfMachines.add(new Machine (jTextField1.getText() , Integer.parseInt(jTextField2.getText()) , jComboBox4.getSelectedIndex()+1 , jComboBox5.getSelectedItem().toString()));
               gym.locker.withdrawAmount(Integer.parseInt(jTextField2.getText()) * (jComboBox4.getSelectedIndex()+1));
               JOptionPane.showMessageDialog(rootPane, "Purchase Successfull!");
                    jDialog1ADD.setVisible(false);
                    jDialog1ADDNEW.setVisible(false);
            }
            else if (jComboBox7.getSelectedItem().toString().equalsIgnoreCase("Weight"))
            {
               gym.listOfWeights.add(new Weight (jTextField1.getText() , Integer.parseInt(jTextField2.getText()) , jComboBox4.getSelectedIndex()+1 , Integer.parseInt(jComboBox6.getSelectedItem().toString())));
               gym.locker.withdrawAmount(Integer.parseInt(jTextField2.getText()) * (jComboBox4.getSelectedIndex()+1));
               JOptionPane.showMessageDialog(rootPane, "Purchase Successfull!");
                    jDialog1ADD.setVisible(false);
                    jDialog1ADDNEW.setVisible(false);
            }
            else
            {
                gym.listOfHandlers.add(new HandlingEquipment (jTextField1.getText() , Integer.parseInt(jTextField2.getText()) , jComboBox4.getSelectedIndex()+1 ));
               gym.locker.withdrawAmount(Integer.parseInt(jTextField2.getText()) * (jComboBox4.getSelectedIndex()+1));
               JOptionPane.showMessageDialog(rootPane, "Purchase Successfull!");
                    jDialog1ADD.setVisible(false);
                    jDialog1ADDNEW.setVisible(false);
            }
          
          }
          
      
      }
        
        
    }//GEN-LAST:event_jButton12ActionPerformed

