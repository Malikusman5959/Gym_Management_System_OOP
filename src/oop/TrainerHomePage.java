    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
      
      String s = "Following errors were found : ";
        
      if (HelperMethods.isAlpha(jTextField25.getText()) == false)
      {
          s += "\n - Invalid Name."; 
      }
      if (jTextField26.getText().length() != 12 || jTextField26.getText().charAt(0) != '0' || jTextField26.getText().charAt(1) != '3' || jTextField26.getText().charAt(4) != '-' || HelperMethods.isNumber(jTextField26.getText().substring(0,4)) != true || HelperMethods.isNumber(jTextField26.getText().substring(5,12)) != true)
      {
          s += "\n - Invalid Cell No.";
      }
      
      if (s.length() > 35)
      {
          //display error message
          JOptionPane.showMessageDialog(rootPane, s);
      }
      else
      {
          //display  succful message
          t.setName(jTextField25.getText());
          t.setContactNo(jTextField26.getText());
          t.shiftType = jComboBox3.getSelectedItem().toString();
          
          JOptionPane.showMessageDialog(rootPane, "Information Updated Successfully!");
          jDialog1MODIFYProfile.setVisible(false);
      }
    }//GEN-LAST:event_jButton5ActionPerformed

