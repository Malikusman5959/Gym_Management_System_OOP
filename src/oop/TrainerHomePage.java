    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        jDialog1VIEWTRAINEES.setVisible(true);
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //clear table
        while (model.getRowCount() > 0)
        {
            for (int i = 0 ; i < model.getRowCount() ; i++)
            {
                model.removeRow(i);
            }
        }
        
        //insertion in table
        Object rowData [] = new Object[5];
        
        for(int i = 0 ; i < t.listOfTrainees.size() ; i++)
        {
            
            rowData [0] = t.listOfTrainees.get(i).regNo;
            rowData [1] = t.listOfTrainees.get(i).name;
            rowData [2] = t.listOfTrainees.get(i).gender;
            rowData [3] = t.listOfTrainees.get(i).sessionType+"";
            rowData [4] = t.listOfTrainees.get(i).ProgramType+"";
            
            model.addRow(rowData);
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

