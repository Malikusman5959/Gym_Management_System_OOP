    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (jTextField7.getText().equalsIgnoreCase(t.getPassword()) == true && jTextField9.getText().equalsIgnoreCase(jTextField10.getText()) && HelperMethods.isValidPassword(jTextField10.getText()) == true)
        {
            //change password
            t.setPassword(jTextField10.getText());
            JOptionPane.showMessageDialog(rootPane, "Password Changed Successfully!");
            jDialog1ChangePassword.setVisible(false);
        }
        
        else
        {
            String s = "";
            if (jTextField7.getText().equalsIgnoreCase(t.getPassword()) == false)
            {
                s += "Wrong Old Password!";
            }
            if (HelperMethods.isValidPassword(jTextField10.getText()) == false)
            {
                s += "\nNew Password is not Valid or Secure Enough!\n Try atleast 8-digit Password with atleast 2 digits.";
            }
            if (jTextField9.getText().equalsIgnoreCase(jTextField10.getText()) == false)
            {
                s += "\nNew Passwords are not Same!";
            }
            JOptionPane.showMessageDialog(rootPane, s);
            jTextField9.setText("");
            jTextField7.setText("");
            jTextField10.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

