        // TODO add your handling code here:
        
        
        String s = "Following errors were found : ";
        
      if (HelperMethods.isAlpha(jTextField8.getText()) == false)
      {
          s += "\n - Invalid Name."; 
      }
      if (jTextField15.getText().length() != 12 || jTextField15.getText().charAt(0) != '0' || jTextField15.getText().charAt(1) != '3' || jTextField15.getText().charAt(4) != '-' || HelperMethods.isNumber(jTextField15.getText().substring(0,4)) != true || HelperMethods.isNumber(jTextField15.getText().substring(5,12)) != true)
      {
          s += "\n - Invalid Cell No.";
      }
      
      if ( jTextField17.getText().length() != 3 || HelperMethods.isNumber(jTextField17.getText().substring(0,3)) != true)
      {
          s += "\n - Invalid Height.";
      }
      if (jTextField10.getText().length() < 2 || jTextField10.getText().length() > 3 || HelperMethods.isNumber(jTextField10.getText().substring(0)) != true)
      {
          s += "\n - Invalid Weight.";
      }
      
      if (s.length() > 35)
      {
          //display error message
          JOptionPane.showMessageDialog(rootPane, s);
      }
      else
      {
          Trainer trainer = null;
          if (jTextField16.getText().equalsIgnoreCase("none") == false)
          {
              //assign trainer
          }
         
          Trainee trainee = new Trainee (jTextField8.getText() , jComboBox1.getSelectedItem().toString() , jComboBox2.getSelectedItem().toString() + "/" + jComboBox3.getSelectedItem().toString() + "/" + jComboBox4.getSelectedItem().toString() , jTextField15.getText(),jTextField13.getText() , "00000000" , jComboBox5.getSelectedItem().toString() + "/" + jComboBox6.getSelectedItem().toString() + "/" + jComboBox7.getSelectedItem().toString() , Integer.parseInt(jTextField17.getText()) , Integer.parseInt(jTextField10.getText()) , trainer , jComboBox8.getSelectedItem().toString() , jComboBox9.getSelectedItem().toString());
          gym.listOfTrainees.add (trainee);
          gym.locker.addAmount(trainee.monthlyFee);
          
          JOptionPane.showMessageDialog(rootPane, "Trainee Added Successfully! \n RegNo : " + trainee.regNo);
          jDialog1ADD.setVisible(false);
          
          //refresh fields
          jTextField8.setText("");
          jTextField15.setText("");
          jTextField13.setText("");
          jTextField17.setText("");
          jTextField10.setText("");
          jTextField16.setText("None");
      }
