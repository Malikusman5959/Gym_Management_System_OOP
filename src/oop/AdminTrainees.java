    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        
        
        jDialog1VIEWALL.setVisible(true);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //clear table
        while (model.getRowCount() > 0)
        {
            for (int i = 0 ; i < model.getRowCount() ; i++)
            {
                model.removeRow(i);
            }
        }
        
        //insertion in table
        Object rowData [] = new Object[12];
        
        for(int i = 0 ; i < gym.listOfTrainees.size() ; i++)
        {
            rowData [0] = gym.listOfTrainees.get(i).regNo;
            rowData [1] = gym.listOfTrainees.get(i).name;
            rowData [2] = gym.listOfTrainees.get(i).gender;
            rowData [3] = gym.listOfTrainees.get(i).contactNo + "";
            rowData [4] = gym.listOfTrainees.get(i).dateofjoining + "";
            if (gym.listOfTrainees.get(i).trainer != null)
            {
                rowData [5] = gym.listOfTrainees.get(i).trainer.name+"";
            }
            else
            {
                rowData [5] = "None";
            }
            
            rowData [6] = gym.listOfTrainees.get(i).height+"";
            rowData [7] = gym.listOfTrainees.get(i).weight+"";
            rowData [8] = gym.listOfTrainees.get(i).monthlyFee+"";
            rowData [9] = gym.listOfTrainees.get(i).fine+"";
            rowData [10] = gym.listOfTrainees.get(i).sessionType+"";
            rowData [11] = gym.listOfTrainees.get(i).ProgramType+"";
            
            model.addRow(rowData);
        }
    }//GEN-LAST:event_jButton11ActionPerformed

